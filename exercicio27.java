package exercicios;

public class exercicio27 {

    public static void main(String[] args) {

        int x, y;

        x = 2;
        y = 5;

        System.out.print("\nx = " + x);
        x++;

        System.out.print("\n\ny = " + y);
        y--;

        System.out.print("\n\nnovo valor de x = " + x);
        System.out.print("\n\nnovo valor de y = " + y);

        System.out.println("\n\n");
    }
}
